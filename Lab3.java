package com.buzzfreeze.helloworld;

public class Lab3 {
	public static void main(String[] args) {

		int i1 = 20;
		for (int data = 1; data <= 5; data++) {
			i1++;
		}
		System.out.println(i1);
		
		int i2 = 20;
		for (int data = 1; data <= 5; data++) {
			--i2;
		}
		System.out.println(i2);
	}
}
